
public class Games {

	int id;
	String name;
	
	//Constructor
	
	public Games(int id, String name) {
		super();
		this.id = id;
		this.name = name;
	}
	
	//getters and setters 
	
	public int getId() {
		return id;
	}
	
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
